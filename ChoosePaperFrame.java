    private void jcLanguageItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jcLanguageItemStateChanged
       if(evt.getStateChange()==ItemEvent.DESELECTED)
           return;
       boolean result=validateInputs();
       if(result==false)
       {
           JOptionPane.showMessageDialog(null, "Please select any subject!","Error!",JOptionPane.ERROR_MESSAGE);
        return;
       }
       jcExamId.removeAllItems();
       try
       {
           if(ExamDAO.isPaperSet(subjectName)==false)
           {
               JOptionPane.showMessageDialog(null, "No exam set for the "+subjectName+"!","Subject Not Found!",JOptionPane.ERROR_MESSAGE);
                return;
           }
           ArrayList<String> examId=ExamDAO.getExamIdBySubject(UserProfile.getUsername(), subjectName);
           if(examId.isEmpty())
           {
               JOptionPane.showMessageDialog(null, "You have given all the exam for the "+subjectName+"!","Exam Not Found!",JOptionPane.ERROR_MESSAGE);
               return;
           }
           for(String id:examId)
           {
               jcExamId.addItem(id);
           }
       }
       catch(Exception ex)
            {
                JOptionPane.showMessageDialog(null, "Exception In DB:"+ex,"Error!",JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
            }
    }//GEN-LAST:event_jcLanguageItemStateChanged

