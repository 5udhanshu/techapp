    private void btnTakeTestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTakeTestActionPerformed
        //here we are doing this because we are assuming that without selecting any combo box the user directly presses the button as and when entered the frame.
        int count=jcLanguage.getSelectedIndex();
        if(count==0)
        {
                JOptionPane.showMessageDialog(null, "Please choose a subject!","Error!",JOptionPane.ERROR_MESSAGE);
                return;   
        }
        count=jcExamId.getItemCount();
        if(count==0)
        {
                JOptionPane.showMessageDialog(null, "Please select an exam Id!","Error!",JOptionPane.ERROR_MESSAGE);
                return;   
        }
        String subject=jcLanguage.getSelectedItem().toString();
        //here we cant use subjectName because user might direclty click on the button once it has entered the frame.
        String id=jcExamId.getSelectedItem().toString();
        int ans;
        ans=JOptionPane.showConfirmDialog(null,"You have choosen\""+subject+"\" and \""+id+"\" paper!\n Is this okay?","Confirmation!",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
        if(ans==JOptionPane.YES_OPTION)
        {
            try
            {
                int totalQuestion=ExamDAO.getQuestionCountByExam(id.trim());
                ExamPojo exam=new ExamPojo();
                exam.setExamId(id);
                exam.setLanguage(subject);
                exam.setTotalQuestion(totalQuestion);
                TakeTestFrame testFrame=new TakeTestFrame(exam);
                testFrame.setVisible(true);
                this.dispose();
            }
            catch(Exception ex)
            {
                JOptionPane.showMessageDialog(null, "Exception In DB:"+ex,"Error!",JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnTakeTestActionPerformed

