    private void jcbStudentIdItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jcbStudentIdItemStateChanged
        
        //if(evt.getStateChange()==ItemEvent.DESELECTED)
        //return;
        if(evt.getStateChange()==ItemEvent.SELECTED)
        {
           String studentId=jcbStudentId.getSelectedItem().toString();
           try
           {
               ArrayList<String> examId=PerformanceDAO.getAllExamId(studentId);
               jcbExamId.removeAllItems();
               for(String it:examId)
               {
                   jcbExamId.addItem(it);
               }
           }
           catch(Exception ex)
            {
                JOptionPane.showMessageDialog(null, "Exception In DB:"+ex,"Error!",JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_jcbStudentIdItemStateChanged

    private void jcbExamIdItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jcbExamIdItemStateChanged
        if(evt.getStateChange()==ItemEvent.SELECTED)
        {
           String studentId=jcbStudentId.getSelectedItem().toString();
           String examId=jcbExamId.getSelectedItem().toString();
           try
           {
               StudentScorePojo score=PerformanceDAO.getScore(examId, studentId);
               txtLanguage.setText(score.getLanguage());
               txtPerc.setText(String.valueOf(score.getPerc()));
           }
           catch(Exception ex)
            {
                JOptionPane.showMessageDialog(null, "Exception In DB:"+ex,"Error!",JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_jcbExamIdItemStateChanged

