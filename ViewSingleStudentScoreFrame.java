    private void loadStudentId() 
    {
        try
        {
           ArrayList<String> studentId=PerformanceDAO.getAllStudentId();
           if(studentId.isEmpty())
           {
               JOptionPane.showMessageDialog(null, "No student has yet appeared for the exam","No Records!",JOptionPane.INFORMATION_MESSAGE);
           }
           else
           {
               jcbStudentId.removeAllItems();//clears allthe items already present in the combobox.
               for(String it:studentId)
               {
                   jcbStudentId.addItem(it);
               }
           }
        }
        catch(Exception ex)
            {
                JOptionPane.showMessageDialog(null, "Exception In DB:"+ex,"Error!",JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
            }
    }



